package com.biblioteca.entidad;

public class MemorandumSolicitud {

	private String codMemSol, fechaMemSol, destinMemSol, temaMemSol, firmaMemSol;

	
	public String getCodMemSol() {
		return codMemSol;
	}

	public void setCodMemSol(String codMemSol) {
		this.codMemSol = codMemSol;
	}

	public String getFechaMemSol() {
		return fechaMemSol;
	}

	public void setFechaMemSol(String fechaMemSol) {
		this.fechaMemSol = fechaMemSol;
	}

	public String getDestinMemSol() {
		return destinMemSol;
	}

	public void setDestinMemSol(String destinMemSol) {
		this.destinMemSol = destinMemSol;
	}

	public String getTemaMemSol() {
		return temaMemSol;
	}

	public void setTemaMemSol(String temaMemSol) {
		this.temaMemSol = temaMemSol;
	}

	public String getFirmaMemSol() {
		return firmaMemSol;
	}

	public void setFirmaMemSol(String firmaMemSol) {
		this.firmaMemSol = firmaMemSol;
	}
}
